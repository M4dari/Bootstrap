O g1 é um portal de notícias brasileiro mantido pelo Grupo Globo e sob orientação da Central Globo de Jornalismo. Foi lançado em 18 de setembro de 2006, ano que a TV Globo fez 41 anos. O portal disponibiliza o conteúdo de jornalismo das diversas empresas do Grupo Globo - TV Globo, GloboNews, rádio CBN, Jornais O Globo, Extra, Expresso e Valor Econômico, revistas Época e Globo Rural, entre outras - além de reportagens próprias em formato de texto, fotos, áudio e vídeo. Além das cinco redações próprias situadas no Rio de Janeiro, em São Paulo, Brasília, Belo Horizonte e Recife, afiliadas da TV Globo, jornais, revistas, rádios e as agências de Notícias como Agência Estado, Agência France Presse, Associated Press, EFE, New York Times, Lusa, Reuters e Valor Econômico alimentam o portal de notícias, que é atualizado 24 horas por dia.

História

Logotipo utilizado entre 2014 e 2021.
Em 2008, os sites dos telejornais da Globo e a homepage da Globonews passaram a ter o domínio do g1. No fim de 2010, o portal integrou as redes afiliadas ao website. Começou com a integração dos estados do Rio de Janeiro e de São Paulo (Conteúdo da Globo Rio e Globo São Paulo). Em seguida os estados de Minas Gerais (Globo Minas e TV Integração),[1] Paraná (RPC), Bahia (Rede Bahia), Distrito Federal (Globo Brasília) e mais recentemente: Mato Grosso (TV Centro América), Mato Grosso do Sul (TV Morena), Ceará (TV Verdes Mares), Espírito Santo (Rede Gazeta), Goiás (Rede Anhanguera), Paraíba (TV Paraíba e TV Cabo Branco), Amazonas (Rede Amazônica), Pernambuco (Globo Nordeste), Rio Grande do Sul (RBS TV RS), Sergipe (TV Sergipe), Maranhão (Rede Mirante), Pará (Rede Liberal e TV Tapajós)), Rondônia (Rede Amazônica), Santa Catarina (RBS TV SC), Rio Grande do Norte (InterTV Cabugi), Piauí (TV Clube), Alagoas (TV Gazeta), Acre (Rede Amazônica) e regiões de cobertura da TV TEM, da EPTV, da TV Tribuna, da Rede Vanguarda, da InterTV, da TV Rio Sul, da TV Asa Branca e da TV Grande Rio. Em 28 de junho iniciou-se o g1 Tocantins.

As versões do g1 no idioma inglês e espanhol foram lançadas em 11 de junho de 2010 e têm os vídeos legendados em dois idiomas.[2][3]

O g1 também tem a versão mobile e aplicativos para Android e IOS.

Em 18 de setembro de 2021, o g1 completou 15 anos de existência. Dois dias depois, o portal lançou sua nova logomarca, adotando a letra minúscula e mantendo a cor vermelha, se alinhando ao novo visual da TV Globo, lançado no mesmo ano.[4]

