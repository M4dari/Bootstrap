Em 2015, o portal ganhou um programa exclusivo na TV Globo, o G1 em 1 Minuto, com as informações do site em boletins pela manhã e a tarde.

Em 2019, o site passou a ganhar uma plataforma de podcasts, com análises e entrevistas sobre os grandes assuntos do cotidiano.[5]

Em 15 de junho de 2021, o site passou a ter um canal próprio no YouTube, com vídeos atualizados sobre o noticiário.[6]
